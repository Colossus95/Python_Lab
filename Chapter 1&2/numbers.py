#Basic Number datatypes 
#integer
a = 50
b = 20
print (f"\n\nSum = {a+b}")
print (f"Difference = {a-b}")
print (f"Product = {a*b}") 
print (f"Quotient = {a/b}") 

#float
x = 3.14
y = 2.71
print (f"\n\nSum = {x+y}")
print (f"Difference = {x-y}")
print (f"Product = {x*y}")
print (f"Quotient = {x/y}")

#mixed
m = 5
n = 2.5
print (f"\n\nSum = {m+n}")
print (f"Difference = {m-n}")
print (f"Product = {m*n}")
print (f"Quotient = {m/n}")

#Underscores in numbers
Universe_Age = 13_800_000_000
print (f"\n\nThe age of the universe is approximately {Universe_Age} years.")

#Multiple assignment
a, b, c = 1, 2.5, "Hello"
print (f"\n\nInteger: {a}") 
print (f"Float: {b}")
print (f"String: {c}")

#constants
PI = 3.14159
GRAVITY = 9.81
AVOGADRO = 6.022e23
print (f"\n\nPI = {PI}")    
print (f"Gravity = {GRAVITY} m/s^2")
print (f"Avogadro's Number = {AVOGADRO} molecules/mol")

